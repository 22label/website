/** The single mobile <audio>. Deliberately NOT connected to any AudioContext —
 *  that keeps it on iOS's media channel (audible with the mute switch ON). */
function ensureMediaEl(): HTMLAudioElement | null {
  if (mediaEl) return mediaEl;
  if (typeof Audio === "undefined") return null;
  const el = new Audio();
  el.src = AUDIO_URL;
  el.loop = true; // native gapless-enough loop (the visual loop uses currentTime % dur)
  el.volume = VOLUME; // fixed 0.6
  el.preload = "auto";
  el.setAttribute("playsinline", "");
  (el as HTMLAudioElement & { playsInline?: boolean }).playsInline = true;
  el.addEventListener("play", () => notifyPlaying(true));
  el.addEventListener("playing", () => notifyPlaying(true));
  el.addEventListener("pause", () => notifyPlaying(false));
  el.addEventListener("ended", () => notifyPlaying(false));
  mediaEl = el;
  return el;
}

/** Load the compact offline spectral asset once (meta then bin). No AudioContext. */
function loadSpectrum(): void {
  if (spectrum || spectrumLoading || typeof fetch === "undefined") return;
  spectrumLoading = true;
  fetch(SPECTRUM_META_URL)
    .then((r) => r.json() as Promise<SpectrumMeta>)
    .then((meta) => {
      spectrumMeta = meta;
      telemetry.analysisFps = meta.analysisFps;
      return fetch(SPECTRUM_BIN_URL).then((r) => r.arrayBuffer());
    })
    .then((buf) => {
      spectrum = new Uint8Array(buf);
      telemetry.analysisLoaded = true;
    })
    .catch(() => {
      spectrumLoading = false;
    });
}

/** Sample the offline spectrum at the media element's current playback time,
 *  interpolating between the two adjacent analysis frames (loops via
 *  currentTime % duration). Fills mBands[] + mBMH; returns whether audio is
 *  actually playing. Zero per-frame allocation. */
function sampleMobile(): boolean {
  const el = mediaEl;
  const spec = spectrum;
  if (!el || !spec || !spectrumMeta) return false;
  const running = !el.paused && !el.ended && el.readyState >= 2;
  if (!running) return false;
  const { analysisFps, frames, frameStride, durationSec, bandCount, layout } =
    spectrumMeta;
  const dur = durationSec > 0 ? durationSec : frames / analysisFps;
  let ct = el.currentTime;
  if (!(ct >= 0)) ct = 0;
  const pos = ct % dur; // seamless visual loop
  const vf = pos * analysisFps; // fractional frame
  let ia = Math.floor(vf);
  const mix = vf - ia;
  if (ia >= frames) ia = frames - 1;
  let ib = ia + 1;
  if (ib >= frames) ib = 0; // wrap to the loop start (no jump at the seam)
  const baseA = ia * frameStride;
  const baseB = ib * frameStride;
  const n = Math.min(HM_N, bandCount);
  for (let i = 0; i < n; i++) {
    const a = spec[baseA + i];
    const b = spec[baseB + i];
    mBands[i] = (a + (b - a) * mix) / 255;
  }
  const at = (off: number) => {
    const a = spec[baseA + off];
    const b = spec[baseB + off];
    return (a + (b - a) * mix) / 255;
  };
  mBMH.bass = at(layout.bass);
  mBMH.mid = at(layout.mid);
  mBMH.high = at(layout.high);
  mBMH.rms = at(layout.rms);
  mBMH.peak = at(layout.peak);
  telemetry.analysisFrame = ia;
  return true;
}

// ============================================================================
//  LIVE_WEB_AUDIO — the original desktop engine (UNCHANGED behaviour)
// ============================================================================

/** Create the single audio graph + start decoding the loop (idempotent). */
function ensureLiveGraph(): boolean {
  if (graph) return true;
  const AC: typeof AudioContext =
    window.AudioContext ||
    (window as unknown as { webkitAudioContext: typeof AudioContext })
      .webkitAudioContext;
  if (!AC) return false;
  let ctx: AudioContext;
  try {
    ctx = new AC();
  } catch {
    return false;
  }
  const gain = ctx.createGain();
  gain.gain.value = VOLUME;
  const analyser = ctx.createAnalyser();
  analyser.fftSize = isMobile() ? PULSE.fftSizeMobile : PULSE.fftSizeDesktop;
  analyser.smoothingTimeConstant = PULSE.smoothing;
  gain.connect(analyser);
  analyser.connect(ctx.destination);
  graph = {
    ctx,
    gain,
    analyser,
    freq: new Uint8Array(new ArrayBuffer(analyser.frequencyBinCount)),
    buffer: null,
    source: null,
    loopDuration: 0,
  };
  if (!visibilityHooked) {
    visibilityHooked = true;
    document.addEventListener("visibilitychange", () => {
      if (document.hidden) zeroOut();
      else if (graph && graph.source && graph.ctx.state === "suspended")
        graph.ctx.resume().catch(() => {}); // resume playback returning to fg
    });
  }
  // Decode the loop once, off the audio thread. Reuse the MP3 bytes prefetched
  // at mount (no download wait on the first tap); fall back to fetching now.
  // Use the CALLBACK form of decodeAudioData wrapped in a Promise — the promise
  // form is unreliable on older iOS Safari / webkitAudioContext.
  if (!decoding) {
    decoding = true;
    const bytes: Promise<ArrayBuffer> = pendingArrayBuffer
      ? Promise.resolve(pendingArrayBuffer)
      : fetch(AUDIO_URL).then((r) => r.arrayBuffer());
    bytes
      .then(
        (a) =>
          new Promise<AudioBuffer>((resolve, reject) => {
            ctx.decodeAudioData(a, resolve, reject);
          }),
      )
      .then((decoded) => {
        if (!graph) return;
        graph.buffer = makeSeamlessLoop(ctx, decoded);
        graph.loopDuration = graph.buffer.length / graph.buffer.sampleRate;
        telemetry.loopStart = 0;
        telemetry.loopEnd = graph.loopDuration;
        telemetry.loopDuration = graph.loopDuration;
        if (autoplayOnce) {
          autoplayOnce = false;
          graph.ctx
            .resume()
            .then(() => {
              if (graph && graph.ctx.state === "running" && !unlocked) {
                wantPlay = true;
                reconcile();
              }
            })
            .catch(() => {});
        } else {
          reconcile(); // a toggle before decode set the desired state
        }
      })
      .catch(() => {
        decoding = false;
      });
  }
  return true;
}

/** Current musical offset (seconds) within the seamless loop. */
function currentOffset(): number {
  if (!graph || !graph.source) return resumeOffset;
  const elapsed = graph.ctx.currentTime - srcStartCtxTime;
  return (srcStartOffset + elapsed) % graph.loopDuration;
}

/** Create a fresh looping source at `offset` with a tiny fade-in. */
function startSource(offset: number): void {
  if (!graph || !graph.buffer || graph.source) return;
  const { ctx, gain, buffer } = graph;
  const src = ctx.createBufferSource();
  src.buffer = buffer;
  src.loop = true;
  src.loopStart = 0;
  src.loopEnd = graph.loopDuration;
  src.connect(gain);
  const t = ctx.currentTime;
  gain.gain.cancelScheduledValues(t);
  gain.gain.setValueAtTime(0.0001, t);
  gain.gain.linearRampToValueAtTime(VOLUME, t + FADE_S);
  src.start(t, offset % graph.loopDuration);
  srcStartCtxTime = t;
  srcStartOffset = offset % graph.loopDuration;
  graph.source = src;
  notifyPlaying(true);
}

/** Stop the source keeping the musical position (short fade -> no click). */
function stopSource(): void {
  if (!graph || !graph.source) return;
  resumeOffset = currentOffset();
  const { ctx, gain } = graph;
  const s = graph.source;
  graph.source = null;
  const t = ctx.currentTime;
  gain.gain.cancelScheduledValues(t);
  gain.gain.setValueAtTime(gain.gain.value, t);
  gain.gain.linearRampToValueAtTime(0.0001, t + FADE_S);
  try {
    s.stop(t + FADE_S + 0.005);
  } catch {
    /* ignore */
  }
  s.onended = () => {
    try {
      s.disconnect();
    } catch {
      /* ignore */
    }
  };
  notifyPlaying(false);
}

/** iOS Safari unlock (LIVE mode only): resume + start a 1-sample silent buffer
 *  SYNCHRONOUSLY in the gesture so a later real source start plays normally. */
function unlock(): void {
  if (!graph) return;
  graph.ctx.resume().catch(() => {});
  if (unlocked) return;
  try {
    const b = graph.ctx.createBuffer(1, 1, graph.ctx.sampleRate);
    const s = graph.ctx.createBufferSource();
    s.buffer = b;
    s.connect(graph.ctx.destination);
    s.start(0);
    s.onended = () => {
      try {
        s.disconnect();
      } catch {
        /* ignore */
      }
    };
    unlocked = true;
  } catch {
    /* ignore */
  }
}

/** Bring actual playback in line with the desired state `wantPlay` (LIVE). */
function reconcile(): void {
  if (!graph || !graph.buffer) return;
  if (wantPlay && !graph.source) startSource(resumeOffset);
  else if (!wantPlay && graph.source) stopSource();
}

