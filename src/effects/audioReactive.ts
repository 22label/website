/** Create/prepare the active engine (idempotent). */
export function ensureAudio(): boolean {
  if (audioMode() === "PRECOMPUTED_MOBILE") {
    ensureMediaEl();
    loadSpectrum();
    return true;
  }
  return ensureLiveGraph();
}

/** Prefetch playback assets at mount WITHOUT creating an AudioContext.
 *  LIVE: fetch the MP3 bytes (context is built later, in-gesture on iOS).
 *  MOBILE: create the <audio> (preload=auto downloads it) + load the spectrum. */
export function prefetchAudio(): void {
  if (audioMode() === "PRECOMPUTED_MOBILE") {
    ensureMediaEl();
    loadSpectrum();
    return;
  }
  if (prefetching || pendingArrayBuffer || typeof fetch === "undefined") return;
  prefetching = true;
  fetch(AUDIO_URL)
    .then((r) => r.arrayBuffer())
    .then((a) => {
      pendingArrayBuffer = a;
    })
    .catch(() => {
      prefetching = false;
    });
}

/** Resume/unlock on a valid user gesture. LIVE: resume + silent-buffer unlock.
 *  MOBILE: just make sure the element + spectrum exist (playback starts on the
 *  player toggle, which is itself a gesture). Never auto-starts music. */
export function userGesture(): void {
  ensureAudio();
  if (audioMode() === "PRECOMPUTED_MOBILE") return;
  if (!graph) return;
  unlock();
  telemetry.audioState = graph.ctx.state;
}

function notifyPlaying(on: boolean): void {
  if (playingTarget === on) {
    // still (re)arm the internal driver if needed (e.g. mobile 'playing' repeat)
    if (on && !externalDriver && !internalRaf) startInternal();
    return;
  }
  playingTarget = on;
  telemetry.playing = on;
  playListeners.forEach((cb) => cb(on));
  if (on && !externalDriver) startInternal();
}

/** Pause: keep the position (no restart on resume). */
export function pausePlayback(): void {
  if (audioMode() === "PRECOMPUTED_MOBILE") {
    mediaEl?.pause();
    return;
  }
  wantPlay = false;
  reconcile();
}

/** ON/OFF toggle used by the player control. Runs inside the click gesture. */
export function togglePlayback(): void {
  if (audioMode() === "PRECOMPUTED_MOBILE") {
    const el = ensureMediaEl();
    loadSpectrum();
    if (!el) return;
    if (el.paused) {
      el.volume = VOLUME;
      const p = el.play();
      // Optimistic ON so the UI flips immediately; the 'play'/'pause' listeners
      // reconcile with reality (iOS rejects only outside a gesture — we're in one).
      notifyPlaying(true);
      if (p && typeof p.then === "function") {
        p.then(() => {
          telemetry.mediaError = "";
        }).catch((err: unknown) => {
          telemetry.mediaError = String(
            (err as { name?: string })?.name || err,
          );
          notifyPlaying(false);
        });
      }
    } else {
      el.pause();
    }
    return;
  }
  ensureLiveGraph();
  if (!graph) return;
  autoplayOnce = false; // user is now in explicit control
  unlock();
  wantPlay = !isPlaying();
  reconcile();
}

/** Exactly one audible source ever. */
export function isPlaying(): boolean {
  if (audioMode() === "PRECOMPUTED_MOBILE")
    return !!mediaEl && !mediaEl.paused;
  return !!(graph && graph.source);
}

/** Subscribe to play-state changes (for React `playing`). Returns unsubscribe. */
export function subscribePlaying(cb: (p: boolean) => void): () => void {
  playListeners.add(cb);
  return () => playListeners.delete(cb);
}

/** The Monogram loop claims/releases the single rAF driver. */
export function setExternalDriver(on: boolean): void {
  externalDriver = on;
  if (on) stopInternal();
  else if (playingTarget || env > 0.001) startInternal();
}

/** Smoothed bands already scaled by the fade envelope (0 when silent/paused). */
export function getBands(): { bass: number; mid: number; high: number } {
  return {
    bass: bands.bass * env,
    mid: bands.mid * env,
    high: bands.high * env,
  };
}

/** Reused array of the spatially-blended band energies (0..1, low freq -> high,
 *  NOT env-scaled; the shader applies the fade). Feeds the continuous field. */
export function getHeatmap(): Float32Array {
  return hmField;
}
/** Play/pause fade envelope (0..1) — the heatmap uses it for fade in/out. */
export function getEnv(): number {
  return env;
}

