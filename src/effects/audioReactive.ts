/**
 * Audio engine + audio-reactive data — TWO pipelines behind ONE API.
 *
 *   LIVE_WEB_AUDIO   (desktop): the original, UNCHANGED engine. ONE AudioContext,
 *     ONE GainNode (0.6), ONE AnalyserNode. The MP3 is decoded once into an
 *     AudioBuffer with a ~12ms baked head-tail crossfade and looped through an
 *     AudioBufferSourceNode (sample-accurate, gapless). The analyser feeds
 *     bass/mid/high + the spectral heatmap.
 *
 *   PRECOMPUTED_MOBILE (iPhone/iPad/touch): playback is a PLAIN HTMLAudioElement
 *     — NEVER routed through createMediaElementSource()/AudioContext — so it
 *     stays on iOS's media channel and is audible even with the mute switch ON.
 *     Because that element can't be analysed without pulling it back onto the
 *     (muteable) Web Audio session, the Sonic Pulse + heatmap are driven by an
 *     OFFLINE spectral asset (public/audio/intruder-spectrum.bin, generated by
 *     scripts/generate-audio-analysis.mjs) sampled by `audio.currentTime` and
 *     interpolated between the two adjacent frames. No AudioContext is created.
 *
 * The mode is decided ONCE per session (device, not viewport) so a mid-session
 * resize can never spawn a second audible source. Everything downstream
 * (getBands/getEnv/getHeatmap/tick) is identical for both modes.
 */
import { EFFECTS, HEATMAP, PULSE, telemetry } from "./effectsConfig";

const AUDIO_URL = "/audio/intruder-snippet.mp3";
const SPECTRUM_BIN_URL = "/audio/intruder-spectrum.bin";
const SPECTRUM_META_URL = "/audio/intruder-spectrum-meta.json";
const CROSSFADE_MS = 12; // baked head-tail crossfade to kill the junction click
const VOLUME = 0.6; // fixed base volume (unchanged, both modes)
const FADE_S = 0.008; // pause/resume click-guard fade

export type AudioMode = "LIVE_WEB_AUDIO" | "PRECOMPUTED_MOBILE";

// The mode is resolved ONCE and cached — never re-evaluated on resize, so a
// viewport change during the session can't switch engines / duplicate audio.
let MODE: AudioMode | null = null;
function detectMode(): AudioMode {
  if (typeof window === "undefined" || typeof navigator === "undefined")
    return "LIVE_WEB_AUDIO";
  const ua = navigator.userAgent || "";
  const isIOS =
    /iP(hone|od|ad)/.test(ua) ||
    (navigator.platform === "MacIntel" && navigator.maxTouchPoints > 1); // iPadOS 13+
  const isTouch =
    "ontouchstart" in window || navigator.maxTouchPoints > 0;
  // Touch/iOS -> precomputed mobile pipeline. A non-touch desktop stays on the
  // LIVE Web Audio engine at ANY width (desktop invariato — a narrow window
  // never flips the engine).
  return isIOS || isTouch ? "PRECOMPUTED_MOBILE" : "LIVE_WEB_AUDIO";
}
export function audioMode(): AudioMode {
  if (!MODE) {
    MODE = detectMode();
    telemetry.mode = MODE;
  }
  return MODE;
}

type Graph = {
  ctx: AudioContext;
  gain: GainNode;
  analyser: AnalyserNode;
  freq: Uint8Array<ArrayBuffer>;
  buffer: AudioBuffer | null; // seamless loop buffer (null until decoded)
  source: AudioBufferSourceNode | null; // the ONE playing source (or null)
  loopDuration: number; // seconds
};

// --- LIVE_WEB_AUDIO state (desktop) -----------------------------------------
let graph: Graph | null = null;
let decoding = false;
let pendingArrayBuffer: ArrayBuffer | null = null; // prefetched MP3 bytes (mount)
let prefetching = false;
let resumeOffset = 0; // musical offset (s) to (re)start from
let srcStartCtxTime = 0; // ctx.currentTime when the current source started
let srcStartOffset = 0; // offset the current source started at
let wantPlay = false; // the app's desired state (should audio be playing?)
let autoplayOnce = true; // one-shot autoplay attempt at load (after decode)
let unlocked = false; // iOS Web Audio unlocked (silent buffer played in a gesture)

// --- PRECOMPUTED_MOBILE state (HTMLAudioElement + offline spectrum) ----------
let mediaEl: HTMLAudioElement | null = null;
let spectrum: Uint8Array | null = null; // flat Uint8: frames × frameStride
type SpectrumMeta = {
  analysisFps: number;
  bandCount: number;
  frameStride: number;
  frames: number;
  durationSec: number;
  layout: {
    bands: [number, number];
    bass: number;
    mid: number;
    high: number;
    rms: number;
    peak: number;
  };
};
let spectrumMeta: SpectrumMeta | null = null;
let spectrumLoading = false;
// Interpolated current-frame values (reused, never allocated per frame).
const mBands = new Float32Array(HEATMAP.numBands);
const mBMH = { bass: 0, mid: 0, high: 0, rms: 0, peak: 0 };

// --- shared play-state ------------------------------------------------------
const playListeners = new Set<(p: boolean) => void>();
let playingTarget = false; // desired state (real playback)
let env = 0; // 0..1 fade envelope (in on play, out on pause)
const bands = { bass: 0, mid: 0, high: 0 }; // smoothed (curved/normalized), pre-envelope
const peaks = { bass: PULSE.peakFloor, mid: PULSE.peakFloor, high: PULSE.peakFloor };

// --- Spectral heatmap: all arrays reused, never allocated in the loop --------
const HM_N = HEATMAP.numBands;
const hmSmooth = new Float32Array(HM_N); // temporally smoothed per-band energy (0..1)
const hmRaw = new Float32Array(HM_N); // raw band energy scratch (reused each frame)
const hmField = new Float32Array(HM_N); // spatially blurred output (getHeatmap)
let hmNorm = HEATMAP.normFloor; // adaptive normalization peak
let externalDriver = false; // true while the Monogram loop drives tick()
let internalRaf = 0;
let internalLast = 0;
let visibilityHooked = false;

const isMobile = () =>
  typeof window !== "undefined" && window.innerWidth <= 767;

/** Bake a seamless loop: crossfade the tail into the head once, so the source's
 *  loop wrap (last sample -> first) is continuous and the junction never clicks.
 *  Sacrifices the final CROSSFADE_MS as blend material only. */
function makeSeamlessLoop(ctx: BaseAudioContext, src: AudioBuffer): AudioBuffer {
  const sr = src.sampleRate;
  const ch = src.numberOfChannels;
  const L = Math.min(Math.round((sr * CROSSFADE_MS) / 1000), src.length >> 2);
  const M = src.length - L; // loop length
  const out = ctx.createBuffer(ch, M, sr);
  for (let c = 0; c < ch; c++) {
    const s = src.getChannelData(c);
    const d = out.getChannelData(c);
    for (let i = L; i < M; i++) d[i] = s[i]; // bulk
    // head[0..L) = equal-power blend of tail (fading out) into head (fading in);
    // d[0] = tail start (=s[M]) so d[M-1](=s[M-1]) -> d[0](=s[M]) is continuous.
    for (let i = 0; i < L; i++) {
      const w = (i + 0.5) / L; // 0..1
      const wi = Math.sin((w * Math.PI) / 2); // head gain
      const wo = Math.cos((w * Math.PI) / 2); // tail gain
      d[i] = s[i] * wi + s[M + i] * wo;
    }
  }
  return out;
}

