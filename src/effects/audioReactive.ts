/** Resume/unlock on a valid user gesture. LIVE: resume + silent-buffer unlock.
 *  MOBILE: just make sure the element + spectrum exist (playback starts on the
 *  player toggle, which is itself a gesture). Never auto-starts music. */
export function userGesture(): void {
  ensureAudio();
  if (audioMode() === "PRECOMPUTED_MOBILE") return;
  if (!graph) return;
  unlock();
  telemetry.audioState = graph.ctx.state;
}

function notifyPlaying(on: boolean): void {
  if (playingTarget === on) {
    // still (re)arm the internal driver if needed (e.g. mobile 'playing' repeat)
    if (on && !externalDriver && !internalRaf) startInternal();
    return;
  }
  playingTarget = on;
  telemetry.playing = on;
  playListeners.forEach((cb) => cb(on));
  if (on && !externalDriver) startInternal();
}

/** Pause: keep the position (no restart on resume). */
export function pausePlayback(): void {
  if (audioMode() === "PRECOMPUTED_MOBILE") {
    mediaEl?.pause();
    return;
  }
  wantPlay = false;
  reconcile();
}

/** ON/OFF toggle used by the player control. Runs inside the click gesture. */
export function togglePlayback(): void {
  if (audioMode() === "PRECOMPUTED_MOBILE") {
    const el = ensureMediaEl();
    loadSpectrum();
    if (!el) return;
    if (el.paused) {
      el.volume = VOLUME;
      const p = el.play();
      // Optimistic ON so the UI flips immediately; the 'play'/'pause' listeners
      // reconcile with reality (iOS rejects only outside a gesture — we're in one).
      notifyPlaying(true);
      if (p && typeof p.then === "function") {
        p.then(() => {
          telemetry.mediaError = "";
        }).catch((err: unknown) => {
          telemetry.mediaError = String(
            (err as { name?: string })?.name || err,
          );
          notifyPlaying(false);
        });
      }
    } else {
      el.pause();
    }
    return;
  }
  ensureLiveGraph();
  if (!graph) return;
  autoplayOnce = false; // user is now in explicit control
  unlock();
  wantPlay = !isPlaying();
  reconcile();
}

/** Exactly one audible source ever. */
export function isPlaying(): boolean {
  if (audioMode() === "PRECOMPUTED_MOBILE")
    return !!mediaEl && !mediaEl.paused;
  if (transport() === "worklet") return workletPlaying;
  return !!(graph && graph.source);
}

