/** The single mobile <audio>. Deliberately NOT connected to any AudioContext —
 *  that keeps it on iOS's media channel (audible with the mute switch ON). */
function ensureMediaEl(): HTMLAudioElement | null {
  if (mediaEl) return mediaEl;
  if (typeof Audio === "undefined") return null;
  const el = new Audio();
  el.src = AUDIO_URL;
  el.loop = true; // native gapless-enough loop (the visual loop uses currentTime % dur)
  el.volume = VOLUME; // fixed 0.6
  el.preload = "auto";
  el.setAttribute("playsinline", "");
  (el as HTMLAudioElement & { playsInline?: boolean }).playsInline = true;
  el.addEventListener("play", () => notifyPlaying(true));
  el.addEventListener("playing", () => notifyPlaying(true));
  el.addEventListener("pause", () => notifyPlaying(false));
  el.addEventListener("ended", () => notifyPlaying(false));
  mediaEl = el;
  return el;
}

/** Load the compact offline spectral asset once (meta then bin). No AudioContext. */
function loadSpectrum(): void {
  if (spectrum || spectrumLoading || typeof fetch === "undefined") return;
  spectrumLoading = true;
  fetch(SPECTRUM_META_URL)
    .then((r) => r.json() as Promise<SpectrumMeta>)
    .then((meta) => {
      spectrumMeta = meta;
      telemetry.analysisFps = meta.analysisFps;
      return fetch(SPECTRUM_BIN_URL).then((r) => r.arrayBuffer());
    })
    .then((buf) => {
      spectrum = new Uint8Array(buf);
      telemetry.analysisLoaded = true;
    })
    .catch(() => {
      spectrumLoading = false;
    });
}

/** Sample the offline spectrum at the media element's current playback time,
 *  interpolating between the two adjacent analysis frames (loops via
 *  currentTime % duration). Fills mBands[] + mBMH; returns whether audio is
 *  actually playing. Zero per-frame allocation. */
function sampleMobile(): boolean {
  const el = mediaEl;
  const spec = spectrum;
  if (!el || !spec || !spectrumMeta) return false;
  const running = !el.paused && !el.ended && el.readyState >= 2;
  if (!running) return false;
  const { analysisFps, frames, frameStride, durationSec, bandCount, layout } =
    spectrumMeta;
  const dur = durationSec > 0 ? durationSec : frames / analysisFps;
  let ct = el.currentTime;
  if (!(ct >= 0)) ct = 0;
  const pos = ct % dur; // seamless visual loop
  const vf = pos * analysisFps; // fractional frame
  let ia = Math.floor(vf);
  const mix = vf - ia;
  if (ia >= frames) ia = frames - 1;
  let ib = ia + 1;
  if (ib >= frames) ib = 0; // wrap to the loop start (no jump at the seam)
  const baseA = ia * frameStride;
  const baseB = ib * frameStride;
  const n = Math.min(HM_N, bandCount);
  for (let i = 0; i < n; i++) {
    const a = spec[baseA + i];
    const b = spec[baseB + i];
    mBands[i] = (a + (b - a) * mix) / 255;
  }
  const at = (off: number) => {
    const a = spec[baseA + off];
    const b = spec[baseB + off];
    return (a + (b - a) * mix) / 255;
  };
  mBMH.bass = at(layout.bass);
  mBMH.mid = at(layout.mid);
  mBMH.high = at(layout.high);
  mBMH.rms = at(layout.rms);
  mBMH.peak = at(layout.peak);
  telemetry.analysisFrame = ia;
  return true;
}

