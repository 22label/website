/** Bake a seamless loop: crossfade the tail into the head once, so the source's
 *  loop wrap (last sample -> first) is continuous and the junction never clicks.
 *  Sacrifices the final CROSSFADE_MS as blend material only. */
function makeSeamlessLoop(ctx: BaseAudioContext, src: AudioBuffer): AudioBuffer {
  const sr = src.sampleRate;
  const ch = src.numberOfChannels;
  const L = Math.min(Math.round((sr * CROSSFADE_MS) / 1000), src.length >> 2);
  const M = src.length - L; // loop length
  const out = ctx.createBuffer(ch, M, sr);
  for (let c = 0; c < ch; c++) {
    const s = src.getChannelData(c);
    const d = out.getChannelData(c);
    for (let i = L; i < M; i++) d[i] = s[i]; // bulk
    // head[0..L) = equal-power blend of tail (fading out) into head (fading in);
    // d[0] = tail start (=s[M]) so d[M-1](=s[M-1]) -> d[0](=s[M]) is continuous.
    for (let i = 0; i < L; i++) {
      const w = (i + 0.5) / L; // 0..1
      const wi = Math.sin((w * Math.PI) / 2); // head gain
      const wo = Math.cos((w * Math.PI) / 2); // tail gain
      d[i] = s[i] * wi + s[M + i] * wo;
    }
  }
  return out;
}

// --- HEAT FX builders -------------------------------------------------------
/** Short, light synthetic reverb impulse: exponentially-decaying stereo noise. */
function makeHeatImpulse(
  ctx: BaseAudioContext,
  seconds: number,
  decay: number,
): AudioBuffer {
  const rate = ctx.sampleRate;
  const len = Math.max(1, Math.floor(rate * seconds));
  const buf = ctx.createBuffer(2, len, rate);
  for (let ch = 0; ch < 2; ch++) {
    const d = buf.getChannelData(ch);
    for (let i = 0; i < len; i++) {
      d[i] = (Math.random() * 2 - 1) * Math.pow(1 - i / len, decay);
    }
  }
  return buf;
}

/** Warm saturation curve = tanh(k·x)/k: unity slope at 0 (small signals pass
 *  through untouched) with a gentle symmetric soft-clip on peaks → warm odd
 *  harmonics, and NO small-signal level increase. */
function makeSatCurve(k: number): Float32Array<ArrayBuffer> {
  const n = 1024;
  const c = new Float32Array(new ArrayBuffer(n * 4)); // <ArrayBuffer> for WaveShaper.curve
  for (let i = 0; i < n; i++) {
    const x = (i / (n - 1)) * 2 - 1;
    // tanh(k·x)/k → slope 1 at x=0 (small signals unchanged), soft-clipped peaks.
    c[i] = Math.tanh(k * x) / k;
  }
  return c;
}

/** Build the post-analyser Heat FX stage. Dry is a constant unity path; the three
 *  wet sends start silent (0) and are opened by updateHeat(). Returns the handles
 *  updateHeat automates. Graph: source → hp → lp → gain → analyser → INPUT →
 *  (dry ‖ delay ‖ reverb ‖ saturation) → OUTPUT → destination. */
function buildHeatFx(ctx: AudioContext, tap: AudioNode): HeatFx {
  const input = ctx.createGain();
  input.gain.value = 1;
  const output = ctx.createGain();
  output.gain.value = 1;
  const dry = ctx.createGain();
  dry.gain.value = 1;

  // Shared high-pass feeding the delay + reverb sends so time-smeared low end never
  // muddies the kick/bass (the dry + saturation keep the full-band body).
  const sendHp = ctx.createBiquadFilter();
  sendHp.type = "highpass";
  sendHp.frequency.value = HEAT_SEND_HP_HZ;
  sendHp.Q.value = 0.707;

  const delay = ctx.createDelay(0.5);
  delay.delayTime.value = HEAT_DELAY_S;
  const delayFb = ctx.createGain();
  delayFb.gain.value = HEAT_DELAY_FB;
  const delayWet = ctx.createGain();
  delayWet.gain.value = 0;

  const convolver = ctx.createConvolver();
  convolver.normalize = true;
  convolver.buffer = makeHeatImpulse(ctx, HEAT_REVERB_S, HEAT_REVERB_DECAY);
  const revWet = ctx.createGain();
  revWet.gain.value = 0;

  const satDrive = ctx.createGain();
  satDrive.gain.value = HEAT_SAT_DRIVE;
  const shaper = ctx.createWaveShaper();
  shaper.curve = makeSatCurve(HEAT_SAT_K);
  shaper.oversample = "2x";
  const satMakeup = ctx.createGain();
  satMakeup.gain.value = HEAT_SAT_MAKEUP;
  const satWet = ctx.createGain();
  satWet.gain.value = 0;

  // Safety limiter — brickwall-ish on the summed output. Threshold near 0dBFS with a
  // fast attack, so it is INACTIVE (no gain reduction) at heat=0 where the dry never
  // approaches the ceiling → the dry sound is unchanged; it only catches wet peaks.
  const limiter = ctx.createDynamicsCompressor();
  limiter.threshold.value = -1;
  limiter.knee.value = 0;
  limiter.ratio.value = 20;
  limiter.attack.value = 0.003;
  limiter.release.value = 0.12;

  // Dry — unity at rest (bit-transparent); dips slightly toward max Heat.
  tap.connect(input);
  input.connect(dry);
  dry.connect(output);
  // Delay send: high-passed, bounded feedback (subtle slapback), no low-end mud.
  input.connect(sendHp);
  sendHp.connect(delay);
  delay.connect(delayFb);
  delayFb.connect(delay);
  delay.connect(delayWet);
  delayWet.connect(output);
  // Reverb send: shares the high-pass (light, controlled room).
  sendHp.connect(convolver);
  convolver.connect(revWet);
  revWet.connect(output);
  // Parallel warm saturation send: driven → shaped → made-up → blended (full-band).
  input.connect(satDrive);
  satDrive.connect(shaper);
  shaper.connect(satMakeup);
  satMakeup.connect(satWet);
  satWet.connect(output);

  output.connect(limiter);
  limiter.connect(ctx.destination);
  return {
    input,
    dry,
    sendHp,
    delay,
    delayFb,
    delayWet,
    convolver,
    revWet,
    satDrive,
    shaper,
    satMakeup,
    satWet,
    output,
    limiter,
  };
}

/** Per-frame Heat automation (LIVE/desktop only). Opens the three wet sends toward
 *  their guarded maxima scaled by the authoritative Home scroll heat — but only
 *  while HOME is mounted, not suppressed (future Scratch), and music is running;
 *  otherwise it ramps every send back to fully dry. All moves are setTargetAtTime
 *  (zipper-free); no nodes are created/destroyed. */
function updateHeat(running: boolean): void {
  if (!graph) return;
  const fx = graph.fx;
  // Coexistence policy: dry unless Home-enabled + running + NOT scratch-suppressed.
  // A scratch (setHeatSuppressed) ducks the FX to dry here without touching the
  // scroll-derived telemetry.heat, so the amount is restored the moment it ends.
  const h = heatTarget(heatEnabled, heatSuppressed, running, telemetry.heat);
  const t = graph.ctx.currentTime;
  fx.delayWet.gain.setTargetAtTime(HEAT_DELAY_WET * h, t, HEAT_TAU);
  fx.revWet.gain.setTargetAtTime(HEAT_REVERB_WET * h, t, HEAT_TAU);
  fx.satWet.gain.setTargetAtTime(HEAT_SAT_WET * h, t, HEAT_TAU);
  // Dry dips from unity (h=0 → exactly 1.0, dry unchanged) to HEAT_DRY_MIN at max so
  // the parallel wet reads clearly without an overall level jump.
  fx.dry.gain.setTargetAtTime(1 - (1 - HEAT_DRY_MIN) * h, t, HEAT_TAU);
  telemetry.heatAudio = h; // applied Heat amount (debug: verify 0→1 + wet targets)
}

